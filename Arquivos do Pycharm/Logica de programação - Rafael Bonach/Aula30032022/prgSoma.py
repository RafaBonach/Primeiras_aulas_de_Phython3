# receber dois numeros inteiros e mostrar a soma dos numeros

primeiro = int(input('digite o primeiro numero inteiro:'))

segundo = int(input('digite o segundo numero inteiro:'))

soma = primeiro + segundo
print('a soma de {} com {} é {}'.format(primeiro, segundo, soma))

sub = primeiro - segundo
print('a subitração de {} com {} é {}'.format(primeiro, segundo, sub))

multi = primeiro * segundo
print('a multiplicação de {} com {} é {}'.format(primeiro, segundo, multi))

div = primeiro / segundo
print('a divisão de {} com {} é {}'.format(primeiro, segundo, div))

dobro1 = (primeiro**2)
print('o dobro de {} é {}' .format(primeiro, dobro1))

dobro2 = segundo**2
print('o dobro de {} é {}' .format(segundo, dobro2))


